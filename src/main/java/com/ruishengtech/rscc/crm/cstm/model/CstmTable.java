package com.ruishengtech.rscc.crm.cstm.model;


/**
 * @author Frank
 * 所有注册的动态表
 */
public class CstmTable{

	private String uuid;
	
	private String tableNameDB;
	
	private String tableName;

	public String getUuid() {
		return this.uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getTableNameDB() {
		return this.tableNameDB;
	}

	public void setTableNameDB(String tableNameDB) {
		this.tableNameDB = tableNameDB;
	}

	public String getTableName() {
		return this.tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	
	
	
}
